package br.com.jeporto.designpatterns.prototype;

public abstract class Car implements Cloneable {

	private String id;
	protected String name;

	abstract void start();

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public Object clone() {
		Object clone = null;

		try {
			clone = super.clone();

		} catch (CloneNotSupportedException e) {
			e.printStackTrace();
		}

		return clone;
	}
}
